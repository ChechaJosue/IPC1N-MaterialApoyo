    public void modificarCarro(){
        for (Carro carro : Serializacion.carros) {
            
            /* if (carros.contador < 50){
                carros[carros.contador] = new Carro();
            }
            */
            
            if(carro.getVIN() == this.VIN){
                carro.modificarCarro(this.txtFabricante.getText(), this.txtModelo.getText(), Integer.valueOf(this.txtYear.getText()), Double.valueOf(this.txtPrecio.getText()));
                Serializacion.ventanaAplicacion.cargarDatosTabla();
                break;
            }
        }
    }

